package operators

fun main(){

    /**
     * Operadores de atribuição
     *
     */

    var x = 5

    x += 2 // x = x + 2
    println(x)

    x *= 2  // x + x * 2

    println(x)

    x -= 2 // x = x - 2

    println(x)

    x /= 3 // x = x / 3

    println(x)

    x++
    println(x)

    x--
    println(x)
}